		<h2 class="white">Enabling for aid</h2>

		<p class="text text7 textlarge white">
			In a team of 5 other students we tried to bridge the needs of humanitarian organizations and
			people's will to contribute with donations of items or engagement.
		</p>
		<br />

		<h4 class="text text7 subtext">
			This is a Design Studio project. That means it was carried through during class for Halmstad
			University. The client is still real and this project evolved organically with the client.
		</h4>
		<br />
	</div>
	<div />

	<div class="maintext">
		<div class="text text25">
			<h3 class="handwriting sidetext"><aside>March 2022</aside></h3>

			<h3>The brief</h3>

			<p class="text longtext">
				When Russia invaded Ukraine in the spring of 2022, everyone wanted to help, but the
				logistics of humanitarian organizations only supported <span class="highlight-container"
					><span class="highlight">donations of money.</span></span
				>
				Individuals who wanted to contribute with <span class="underline">items</span> or their
				<span class="underline">engagement</span>
				went unappreciated and had nowhere to turn to. The origin of this observation came from HighFive,
				an independent growth department of Halmstad county in Sweden.
			</p>
			<p class="text longtext">
				Inspired by workshops they had participated in, they presented an idea for how to counter
				this problem. A request based web platform where users could "claim" a request published by
				humanitarian organizations and then fulfill it.
				<br />
				<br />
			</p>
			<h3 class="handwriting sidetext">April 2022</h3>

			<h3>Getting to work</h3>
			<p>
				Following some desktop research, we learned that most humanitarian organizations actually
				contributed in other countries, rather than aiding Swedish refugees when they have arrived.
				Since money is very easy to move, individuals didn't receive the opportunity to contribute
				with other things. The question remained, why should all these people who were willing to
				contribute go unappreciated?
			</p>
			<p>
				Since the brief was rather loosely defined, we decided to discuss a more defined problem
				specification together with HighFive. We discussed what the unique value proposition would be
			</p>
			<p>
				We decided to focus on the needs of the humanitarian organizations and the people who wanted
				to contribute. We wanted to create a platform that would make it easier for humanitarian
				organizations to publish requests and for people to fulfill them. We also wanted to create a
				platform that would make it easier for people to find out what they could do to help.
			</p>
		</div>
	</div>
